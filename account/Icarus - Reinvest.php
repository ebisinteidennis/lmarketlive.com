    <script>
        function getPlan() {
            let category = $("#category").val();
            let name = $("#plan").val();
            $.ajax({
                url: "https://icarusltd.net/fire/get-plan",
                type: 'GET',
                data: {name:name, category:category},
            }).done(function( res ) {
                $('#percentage').val((res.data.percentage)+'%')
                $('#minimum').val('$ '+(res.data.minimum))
                $('#maximum').val('$ '+(res.data.maximum))
                $('#duration').val(Math.round(res.data.duration)+' day(s)')
                $('#amount').val('')
                $('#profit').val('$0')
                $('#total').val('$0')
            }).fail(function() {

            });
        }
    </script>

    <script>
        function calculate(){
            let capital = $('#amount').val().replace(/[^\w\s]/gi, '')
            let interest = $('#percentage').val().replace(/[&\/\#,a-z+()$~%'":*?<>{}]/g, '')
            let duration = $('#duration').val().match(/(\d+)/);
            let percentage = interest/100
            let profit = percentage * capital
            let total = +profit + +capital
            $('#profit').val('$'+Number(profit).toLocaleString('en'))
            $('#total').val('$'+Number(total).toLocaleString('en'))
        }
    </script>

    <script>
        $('#amount').keyup(function(event) {
            // skip for arrow keys
            if(event.which >= 37 && event.which <= 40) return;
            // format number
            $(this).val(function(index, value) {
                return value
                    .replace(/\D/g, "")
                    .replace(/\B(?=(\d{3})+(?!\d))/g, ",")
            });
        });
    </script>
